pub fn decrypt_client_rand_key(b64: &str) -> Option<u64> {
    let ciphertext = BASE64_STANDARD.decode(b64).ok()?;
    if ciphertext.len() != 128 {
        return None;
    }

    let n = BigUint::from_bytes_be(&MODULUS_BYTES);
    let d = BigUint::from_bytes_be(&PRIVATE_EXPONENT_BYTES);
    let c = BigUint::from_bytes_be(&ciphertext);

    let m = c.modpow(&d, &n);
    let mut plaintext = m.to_bytes_be();

    // Pad to 128 bytes if needed
    if plaintext.len() < 128 {
        let mut padded = vec![0; 128 - plaintext.len()];
        padded.extend(plaintext);
        plaintext = padded;
    }

    // Parse PKCS#1 v1.5 padding (0x00 0x02 ... 0x00 payload)
    // The zig code just searches for the first 0x00 starting at index 2
    let mut zero_idx = None;
    for i in 2..plaintext.len() {
        if plaintext[i] == 0 {
            zero_idx = Some(i);
            break;
        }
    }

    let zero_idx = zero_idx?;
    let payload = &plaintext[zero_idx + 1..];

    if payload.len() != 8 {
        return None;
    }

    let mut buf = [0u8; 8];
    buf.copy_from_slice(payload);
    Some(u64::from_le_bytes(buf))
}

pub fn derive_xor_key(server_rand_key: u64, client_rand_key: u64) -> u64 {
    server_rand_key ^ client_rand_key
}
